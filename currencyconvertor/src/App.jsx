function App() {
  const [amount, setAmount] = useState(1); // Default amount in INR
  const [currency, setCurrency] = useState("USD"); // Default target currency
  const [convertedAmount, setConvertedAmount] = useState(0);
  const [currencies, setCurrencies] = useState([]);
  const [error, setError] = useState(null);

  const fetchExchangeRates = async () => {
    try {
      const response = await fetch(
        `https://api.exchangerate-api.com/v4/latest/INR`
      );
      if (!response.ok) {
        throw new Error("Failed to fetch exchange rates.");
      }
      const data = await response.json();
      setCurrencies(Object.keys(data.rates));
      setConvertedAmount((amount * data.rates[currency]).toFixed(2));
    } catch (error) {
      setError(error.message);
    }
  };

  useEffect(() => {
    fetchExchangeRates();
  }, [currency, amount]);

  const handleAmountChange = (e) => {
    setAmount(e.target.value);
  };

  const handleCurrencyChange = (e) => {
    setCurrency(e.target.value);
  };

  return (
    <div className="min-h-screen flex items-center justify-center bg-gradient-to-r from-indigo-500 via-purple-500 to-pink-500">
      <div className="bg-white shadow-lg rounded-lg p-6 max-w-md w-full">
        <h1 className="text-2xl font-bold text-center text-gray-800 mb-4">
          INR Currency Converter By Satyam
        </h1>
        {error && <p className="text-red-500 text-center mb-4">{error}</p>}
        <div className="mb-4">
          <label className="block text-gray-600 font-semibold mb-2">
            Amount in INR:
          </label>
          <input
            type="number"
            value={amount}
            onChange={handleAmountChange}
            className="w-full border border-gray-300 rounded-lg p-2 outline-none focus:ring-2 focus:ring-indigo-500"
          />
        </div>
        <div className="mb-4">
          <label className="block text-gray-600 font-semibold mb-2">
            Convert to:
          </label>
          <select
            value={currency}
            onChange={handleCurrencyChange}
            className="w-full border border-gray-300 rounded-lg p-2 outline-none focus:ring-2 focus:ring-indigo-500"
          >
            {currencies.map((cur) => (
              <option key={cur} value={cur}>
                {currencyMap[cur] || cur} ({cur}) {/* Display full name and code */}
              </option>
            ))}
          </select>
        </div>
        <div className="bg-indigo-100 p-4 rounded-lg text-center">
          <p className="text-gray-800 font-semibold text-lg">
            Converted Amount:
          </p>
          <p className="text-2xl font-bold text-indigo-600">
            {convertedAmount} {currency}
          </p>
        </div>
      </div>
    </div>
  );
}

export default App;
